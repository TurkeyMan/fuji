#define MAX_AUX_DISPLAYS 16
#define MAX_SOFT_KEYS 16

static bool gG15Available = false;
static int gNumDisplays = 0;

static MFAuxDisplayProperties gAuxDeviceProperties[MAX_AUX_DISPLAYS];
static int gDeviceHandles[MAX_AUX_DISPLAYS];
static char gLastKeystate[MAX_AUX_DISPLAYS][MAX_SOFT_KEYS];
static char gSoftKeystate[MAX_AUX_DISPLAYS][MAX_SOFT_KEYS];

static int gConnection;

void MFAuxDisplay_Init(const char *pAppName)
{
	MFZeroMemory(gSoftKeystate, sizeof(gSoftKeystate));
	MFZeroMemory(gLastKeystate, sizeof(gSoftKeystate));

#if defined(SUPPORT_G15)
	DWORD r = lgLcdInit();

	if(r == ERROR_SUCCESS)
		gG15Available = true;
	else
		MFDebug_Warn(3, "Auxiliary display unavailable or failed to initialise.");

	if(gG15Available)
	{
		lgLcdConnectContext context;
		lgLcdDeviceDesc desc;

		context.appFriendlyName = pAppName;
		context.isPersistent = TRUE;
		context.isAutostartable = FALSE;
		context.onConfigure.configCallback = NULL;
		context.onConfigure.configContext = NULL;
		context.connection = 0;

		lgLcdConnect(&context);

		gConnection = context.connection;

		DWORD r;

		do
		{
			r = lgLcdEnumerate(context.connection, gNumDisplays, &desc);

			if(r == ERROR_SUCCESS)
			{
				gAuxDeviceProperties[gNumDisplays].type = MFAuxType_G15;
				gAuxDeviceProperties[gNumDisplays].numButtons = desc.NumSoftButtons;

				gAuxDeviceProperties[gNumDisplays].screenWidth = desc.Width;
				gAuxDeviceProperties[gNumDisplays].screenHeight = desc.Height;
				gAuxDeviceProperties[gNumDisplays].colourDepth = desc.Bpp;

				gAuxDeviceProperties[gNumDisplays].relatedInputDevice = IDD_Keyboard;
				gAuxDeviceProperties[gNumDisplays].relatedInputDeviceID = 0;

				lgLcdOpenContext open;
				open.connection = gConnection;
				open.index = gNumDisplays;
				open.onSoftbuttonsChanged.softbuttonsChangedCallback = NULL;
				open.onSoftbuttonsChanged.softbuttonsChangedContext = NULL;
				open.device = 0;

				r = lgLcdOpen(&open);

				MFDebug_Assert(r == ERROR_SUCCESS, "Failed to open auxillary display device.");

				gDeviceHandles[gNumDisplays] = open.device;

				MFAuxDisplay_UploadImage(gNumDisplays, (const char*)gDefaultImage, MFAuxPriority_High);

				++gNumDisplays;
			}
		}
		while(r == ERROR_SUCCESS && gNumDisplays < MAX_AUX_DISPLAYS);
	}
#endif
}

void MFAuxDisplay_Deinit()
{
#if defined(SUPPORT_G15)
	if(gG15Available)
	{
		for(int a=0; a<gNumDisplays; a++)
			lgLcdClose(gDeviceHandles[a]);

		lgLcdDisconnect(gConnection);

		lgLcdDeInit();
	}
#endif
}

void MFAuxDisplay_Update()
{
	MFCopyMemory(gLastKeystate, gSoftKeystate, sizeof(gLastKeystate));

#if defined(SUPPORT_G15)
	for(int a=0; a<gNumDisplays; a++)
	{
		DWORD buttons;
		lgLcdReadSoftButtons(gDeviceHandles[a], &buttons);
		for(uint32 b=0; b<gAuxDeviceProperties[a].numButtons; b++)
		{
			gSoftKeystate[a][b] = (buttons & (1 << b)) ? 1 : 0;
		}
	}
#endif
}

int MFAuxDisplay_GetNumberOfDisplays()
{
	return gNumDisplays;
}

void MFAuxDisplay_GetDisplayProperties(int device, MFAuxDisplayProperties *pProperties)
{
	MFDebug_Assert(device < gNumDisplays, "Invalid auxillary display.");

	MFCopyMemory(pProperties, &gAuxDeviceProperties[device], sizeof(*pProperties));
}

void MFAuxDisplay_LockScreen(int device, bool lock)
{
#if defined(SUPPORT_G15)
	lgLcdSetAsLCDForegroundApp(gDeviceHandles[device], lock ? LGLCD_LCD_FOREGROUND_APP_YES : LGLCD_LCD_FOREGROUND_APP_NO);
#endif
}

void MFAuxDisplay_UploadImage(int device, const char *pImageBuffer, int priority)
{
#if defined(SUPPORT_G15)
	lgLcdBitmap160x43x1 header;
	header.hdr.Format = LGLCD_BMP_FORMAT_160x43x1;
	MFCopyMemory(header.pixels, pImageBuffer, sizeof(header.pixels));

	lgLcdUpdateBitmap(gDeviceHandles[device], (lgLcdBitmapHeader*)&header, priority <= MFAuxPriority_High ? LGLCD_PRIORITY_ALERT : priority > MFAuxPriority_Normal ? LGLCD_PRIORITY_BACKGROUND : LGLCD_PRIORITY_NORMAL);
#endif
}

int MFAuxDisplay_ReadButton(int device, int button)
{
	return gSoftKeystate[device][button];
}

bool MFAuxDisplay_WasPressed(int device, int button)
{
	return gSoftKeystate[device][button] && !gLastKeystate[device][button];
}

bool MFAuxDisplay_WasReleased(int device, int button)
{
	return !gSoftKeystate[device][button] && gLastKeystate[device][button];
}

